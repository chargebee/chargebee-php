<?php

namespace Chargebee\Responses\RampResponse;
use Chargebee\Resources\Ramp\Ramp;

use Chargebee\ValueObjects\ResponseBase;

class ListRampResponse extends ResponseBase { 
    /**
    *
    * @var array<ListRampResponseListObject> $list
    */
    public array $list;
    
    /**
    *
    * @var ?string $next_offset
    */
    public ?string $next_offset;
    

    private function __construct(
        array $list,
        ?string $next_offset,
        array $responseHeaders=[],
    )
    {
        parent::__construct($responseHeaders);
        $this->list = $list;
        $this->next_offset = $next_offset;
        
    }
    public static function from(array $resourceAttributes, array $headers = []): self
    {
            $list = array_map(function (array $result): ListRampResponseListObject {
                return new ListRampResponseListObject(
                    isset($result['ramp']) ? Ramp::from($result['ramp']) : null,
                );}, $resourceAttributes['list'] ?? []);
        
        return new self($list,
            $resourceAttributes['next_offset'] ?? null, $headers);
    }

    public function toArray(): array
    {
        $data = array_filter([
            'list' => $this->list,
            'next_offset' => $this->next_offset,
        ]);
        return $data;
    }
}


class ListRampResponseListObject {
    
        public Ramp $ramp;
    
public function __construct(
    Ramp $ramp,
){ 
    $this->ramp = $ramp;

}
}

?>