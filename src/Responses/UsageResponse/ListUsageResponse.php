<?php

namespace Chargebee\Responses\UsageResponse;
use Chargebee\Resources\Usage\Usage;

use Chargebee\ValueObjects\ResponseBase;

class ListUsageResponse extends ResponseBase { 
    /**
    *
    * @var array<ListUsageResponseListObject> $list
    */
    public array $list;
    
    /**
    *
    * @var ?string $next_offset
    */
    public ?string $next_offset;
    

    private function __construct(
        array $list,
        ?string $next_offset,
        array $responseHeaders=[],
    )
    {
        parent::__construct($responseHeaders);
        $this->list = $list;
        $this->next_offset = $next_offset;
        
    }
    public static function from(array $resourceAttributes, array $headers = []): self
    {
            $list = array_map(function (array $result): ListUsageResponseListObject {
                return new ListUsageResponseListObject(
                    isset($result['usage']) ? Usage::from($result['usage']) : null,
                );}, $resourceAttributes['list'] ?? []);
        
        return new self($list,
            $resourceAttributes['next_offset'] ?? null, $headers);
    }

    public function toArray(): array
    {
        $data = array_filter([
            'list' => $this->list,
            'next_offset' => $this->next_offset,
        ]);
        return $data;
    }
}


class ListUsageResponseListObject {
    
        public Usage $usage;
    
public function __construct(
    Usage $usage,
){ 
    $this->usage = $usage;

}
}

?>